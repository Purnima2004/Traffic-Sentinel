export interface OwnerDetails {
  owner_name: string | null;
  owner_address: string | null;
  owner_phone: string | null;
  owner_email: string | null;
  vehicle_model: string | null;
}

export function lookupVehicle(plateNumber: string): OwnerDetails {
  if (!plateNumber) {
    return {
      owner_name: null,
      owner_address: null,
      owner_phone: null,
      owner_email: null,
      vehicle_model: null
    };
  }
  
  // Normalize: remove all non-alphanumeric characters and uppercase
  // This handles "MH 12 KN 4567" vs "MH12KN4567"
  const normalizedInput = plateNumber.replace(/[^A-Z0-9]/gi, '').toUpperCase();
  
  const record = RTO_DATABASE.find(r => 
      r.vehicle_number.replace(/[^A-Z0-9]/gi, '').toUpperCase() === normalizedInput
  );

  if (record) {
      return {
          owner_name: record.owner_name,
          owner_address: record.address,
          owner_phone: record.mobile_number,
          owner_email: record.email,
          vehicle_model: record.vehicle_model
      };
  }
  
  return {
      owner_name: null,
      owner_address: null,
      owner_phone: null,
      owner_email: null,
      vehicle_model: null
  };
}
